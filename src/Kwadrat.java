public class Kwadrat {
    public static void main(String[] args) {
        int bok_a = 2;
        System.out.println("Pole powierzchni kwadratu pierwszego wynosi " + (bok_a*2) + "");
        System.out.println("Obwód kwadratu pierwszego wynosi " + (bok_a*4) +"");
        System.out.println("Długość przekątnej kwadratu pierwszego wynosi " + bok_a*Math.sqrt(2) + "");

        int bok_b = 7;
        System.out.println("Pole powierzchni kwadratu drugiego wynosi " + (bok_b*2) + "");
        System.out.println("Obwód kwadratu drugiego wynosi " + (bok_b*4) +"");
        System.out.println("Długość przekątnej kwadratu drugiego wynosi " + bok_b*Math.sqrt(2) + "");

        int bok_c = 11;
        System.out.println("Pole powierzchni kwadratu trzeciego wynosi " + (bok_c*2) + "");
        System.out.println("Obwód kwadratu trzeciego wynosi " + (bok_c*4) +"");
        System.out.println("Długość przekątnej kwadratu trzeciego wynosi " + bok_c*Math.sqrt(2) + "");

        int bok_d = 19;
        System.out.println("Pole powierzchni kwadratu czwartego wynosi " + (bok_d*2) + "");
        System.out.println("Obwód kwadratu czwartego wynosi " + (bok_d*4) +"");
        System.out.println("Długość przekątnej kwadratu czwartego wynosi " + bok_d*Math.sqrt(2) + "");
    }
}
